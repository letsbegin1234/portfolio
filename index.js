function colorChange(x) {
    document.body.style.backgroundColor = x;
    document.body.style.transitionDuration = "3s";
    

}
function bgcolor() {
    document.body.style.backgroundColor = "#D7E9F7";
    document.body.style.transitionDuration = "3s";

}


